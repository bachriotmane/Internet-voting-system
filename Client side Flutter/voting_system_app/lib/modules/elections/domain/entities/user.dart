class UserInfo {
  String username;
  String firstName;
  String lastName;
  String email;
  String phoneNumber;
  String CIN;
  String age;

  UserInfo({
    required this.username,
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.phoneNumber,
    required this.CIN,
    required this.age
  });
}
